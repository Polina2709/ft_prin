#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int n;

	n = 0;
	if (s == NULL || f == NULL)
		return ;
	if (s)
	{
		while (s[n] != '\0')
		{
			(*f)(&s[n]);
			n++;
		}
	}
}
